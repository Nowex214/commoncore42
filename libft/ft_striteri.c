#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	i;

	i = 0;
	if (s)
	{
		while (i, &s[i])
		{
			(*f)(s[i]);
			i++;
		}
	}
}